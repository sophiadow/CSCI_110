def print_day_of_week(x):
    if x == 0:
        print("Sunday")
    elif x == 1:
        print("Monday")
    elif x == 2:
        print("Tuesday")
    elif x == 3:
        print("Wednesday")
    elif x == 4:
        print("Thursday")
    elif x == 5:
        print("Friday")
    else:
        print("Not a weekday")
print_day_of_week(5)




    







